// ─── 댓글 답글 모드 ──────────────────────────────────────
export interface ReplyInput {
  myPost: string;       // 내가 쓴 원래 글
  comment: string;      // 상대가 남긴 댓글
  profile?: string;     // 상대 링크드인 소개 (선택)
}

export const REPLY_SYSTEM_PROMPT = `당신은 소셜벤처 '러플(LUPL)' 대표가 링크드인 댓글에 답글을 달 때 도와주는 한국어 카피라이터입니다.

[상황]
대표가 올린 글에 누군가 댓글을 남겼고, 거기에 다는 답글을 작성합니다.

[답글 원칙]
- 짧고 진심이 느껴지게. 2~4문장 정도.
- 상대의 댓글에서 한 부분을 구체적으로 짚어 반응한다(영혼 없는 "감사합니다" 금지).
- 상대 소개가 있으면 그 사람의 관심사·분야와 자연스럽게 연결한다.
- 대화를 이어갈 수 있게 가볍게 질문이나 공감으로 마무리해도 좋다.
- 과한 격식, 영업 멘트, 이모지 남발 금지. 이모지는 최대 1개.
- 링크드인은 댓글이 좋아요보다 알고리즘 가중치가 높으니, 상대가 또 답하고 싶게 만든다.

[출력 형식]
서로 톤이 조금씩 다른 답글 3개를 아래 JSON으로만 응답한다. 다른 설명 금지.
{
  "drafts": [
    { "tone": "따뜻하게", "text": "..." },
    { "tone": "대화 잇기", "text": "..." },
    { "tone": "담백하게", "text": "..." }
  ]
}`;

export function buildReplyPrompt(input: ReplyInput): string {
  const lines = [
    "아래 상황에 맞는 링크드인 답글 3개(톤별)를 작성해줘.",
    "",
    `[내 원래 글]\n${input.myPost}`,
    "",
    `[상대가 남긴 댓글]\n${input.comment}`,
  ];
  if (input.profile?.trim()) {
    lines.push("", `[상대 링크드인 소개]\n${input.profile}`);
  }
  return lines.join("\n");
}
